                    <article>
                            <em>DESCRIPTION</em></br>
                            Purigen Biosystems, Inc., a Venture-backed, life science tools start-up company in the San Francisco bay area, is seeking an experienced Senior/Staff Scientist to join our product development team as an individual contributor. The Senior/Staff Scientist will drive development of consumable sample preparation reagents and associated workflows for Purigen’s sample preparation technology. This is a challenging position for the highly qualified genomics specialist who wants to develop cutting-edge life science technologies in a fast-paced, start-up environment.<br/><br/>

                    <em>RESPONSIBILITIES</em></br>
                    <ul>
                            <li>Develop and execute bioanalytical characterization methods to validate yield, integrity, purity and performance in downstream applications of nucleic acids extracted from Purigen’s platform.</li>
        <li>Conduct characterization testing and develop system wide user workflows for optimum performance.</li>
        <li>Independently design, plan, execute and analyze experiments to achieve project milestones.</li>
        <li>Serve as a technical genomics expert, with emphasis on NGS library prep and sequence analysis for human disease applications, such as cancer.</li>
        <li>Work closely with R&amp;D chemists, engineers, and marketing to interpret characterization data to guide product development.</li>
        <li>Follow good laboratory practices, including maintenance of accurate laboratory notebooks.</li>
                    </ul>
</br>

                    <em>QUALIFICATIONS</em>
    </br>
    <p><strong>REQUIRED</strong></a>
                    <ul>
                            <li>Ph.D. in Biochemistry, Molecular Biology, or closely related fields with 5 years of prior product development experience in the life science tools industry</li>
        <li>Prior industry experience developing reagents and their associated application(s) for a commercial product</li>
        <li>Prior industry experience with 1 or more full product life cycle(s) in a life sciences tools development role</li>
        <li>Hands-on experience with sample preparation and analytical analyses of nucleic acids extracted from human samples, including FFPE</li>
        <li>Working knowledge of the principles underlying commercial DNA and RNA sample preparation kits (i.e. off-the-shelf solid-phase extraction kits (beads and/or columns) and liquid-phase extraction reagents)</li>
        <li>Strong hands on technical skills in molecular biology and genomics — including amplification, next-generation sequencing (NGS) library prep and target enrichment, and Illumina sequencing</li>
        <li>Ability to work in a fast paced, dynamic environment</li>
                    </ul>
    </br>

    <p><strong>DESIRED</strong></a>
                    <ul>
        <li>Expertise in NGS bioinformatic analysis</li>
        <li>Prior experience with the development of genomics applications on microfluidics-based instrumentation for the life sciences industry</li>
    </ul>
